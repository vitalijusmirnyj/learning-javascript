/*
Write a function that will accept two parameters (text, index) and will return a single letter at the index but changed to the lowercase. 
E.g. yourFunction(‘Hello’, 0) should return ‘h’.
*/

function characterAtIndexToLowerCase(text, index){ 
    return text.charAt().toLowerCase();
}

module.exports.characterAtIndexToLowerCase = characterAtIndexToLowerCase;